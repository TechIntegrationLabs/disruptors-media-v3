// Helper function to get responsive image URL
export const getResponsiveImage = (baseUrl: string, width: number, height?: number) => {
  if (!baseUrl) return '';
  
  const params = [`w_${width}`];
  if (height) params.push(`h_${height}`);
  params.push('c_fill');
  
  // Insert parameters before the filename
  const parts = baseUrl.split('/');
  const uploadIndex = parts.indexOf('upload');
  if (uploadIndex !== -1) {
    parts.splice(uploadIndex + 1, 0, params.join(','));
  }
  
  return parts.join('/');
};

// Helper function to get video with multiple formats
export const getVideoSources = (videoUrl: string) => {
  if (!videoUrl) return [];
  
  return [
    {
      src: videoUrl.replace('/upload/', '/upload/vc_h265/'),
      type: 'video/mp4; codecs=hvc1'
    },
    {
      src: videoUrl,
      type: 'video/mp4'
    }
  ];
};